package com.eucalyptus.util;

public class ExceptionNotRelatedException extends Throwable {

  public ExceptionNotRelatedException( ) {
    super( );
  }

  public ExceptionNotRelatedException( String message, Throwable cause ) {
    super( message, cause );
  }

  public ExceptionNotRelatedException( String message ) {
    super( message );
  }

  public ExceptionNotRelatedException( Throwable cause ) {
    super( cause );
  }

}
