package com.eucalyptus.ws;

public class RemoteConfigurationException extends Exception {

  public RemoteConfigurationException( ) {
    super( );
  }

  public RemoteConfigurationException( String message, Throwable cause ) {
    super( message, cause );
  }

  public RemoteConfigurationException( String message ) {
    super( message );
  }

  public RemoteConfigurationException( Throwable cause ) {
    super( cause );
  }

}
